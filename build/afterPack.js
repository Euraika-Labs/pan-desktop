// eslint-disable-next-line @typescript-eslint/no-require-imports
const { execSync } = require("child_process");
// eslint-disable-next-line @typescript-eslint/no-require-imports
const { createHash } = require("crypto");
// eslint-disable-next-line @typescript-eslint/no-require-imports
const { readFileSync, writeFileSync, existsSync } = require("fs");
// eslint-disable-next-line @typescript-eslint/no-require-imports
const path = require("path");

exports.default = async function afterPack(context) {
  const platform = context.electronPlatformName;

  if (platform === "darwin") {
    const appPath = path.join(
      context.appOutDir,
      `${context.packager.appInfo.productFilename}.app`,
    );
    console.log(`Ad-hoc re-signing: ${appPath}`);
    execSync(`codesign --force --deep --sign - "${appPath}"`, {
      stdio: "inherit",
    });
    return;
  }

  if (platform === "win32") {
    // M1 ships unsigned — see README "Windows SmartScreen notice" and
    // DECISION_LOG.md 2026-04-12 entry. When activating signing in the
    // future, add signtool invocation here or use electron-builder's
    // win.sign custom hook.

    // M1.1-#005: compute SHA256 of the vendored install.ps1 and write a
    // sidecar JSON next to it. The sidecar is primarily for external
    // verification tooling / audit; the RUNTIME check in
    // WindowsInstallerStrategy uses the TypeScript constant generated
    // by `build/generateInstallPs1Hash.js` at prebuild time, which is
    // compiled into app.asar and covered by Electron's asar integrity
    // fuses. This double-binding means an attacker must tamper BOTH
    // files (script + compiled constant in asar) — and tampering the
    // asar trips the integrity fuse.
    const resourcesDir = path.join(context.appOutDir, "resources");
    const scriptPath = path.join(resourcesDir, "install.ps1");
    if (existsSync(scriptPath)) {
      const hash = createHash("sha256")
        .update(readFileSync(scriptPath))
        .digest("hex");
      writeFileSync(
        path.join(resourcesDir, "install.ps1.sha256.json"),
        JSON.stringify(
          {
            sha256: hash,
            algorithm: "sha256",
            note:
              "Sidecar for external verification. Runtime integrity check " +
              "uses the TypeScript constant generated by " +
              "build/generateInstallPs1Hash.js.",
          },
          null,
          2,
        ) + "\n",
      );
      console.log(`[afterPack] install.ps1 sha256 = ${hash}`);
    } else {
      console.warn(
        `[afterPack] install.ps1 not found at ${scriptPath} — skipping hash`,
      );
    }
    return;
  }

  // Linux and others: no post-pack signing.
};
